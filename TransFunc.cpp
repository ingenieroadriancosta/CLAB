double SegundoOrden( double t, double Gain, double W0, double zeta ){
double Res = 0;
double ArgExp = - (W0 * t * zeta)/2;

double ArgSQRT = 0;
ArgSQRT = (W0*W0);
ArgSQRT = (ArgSQRT * zeta*zeta);
ArgSQRT = ArgSQRT/4;
ArgSQRT = ArgSQRT - W0*W0;
ArgSQRT = sqrt( ArgSQRT );
ArgSQRT = t * ArgSQRT;

double ArgCH = cosh( ArgSQRT );
double ArgSH = sinh( ArgSQRT );

double ArgNum = W0 * zeta * ArgSH;
double ArgDen = 2 * ArgSQRT;

if( ArgDen<0.00000000001 ){ArgDen=0.01;}
Res = Gain - Gain*exp(ArgExp) * ( ArgCH + ArgNum/ArgDen );
/*
                                 /                                                /   /   2     2       \1/2 \ \
                                 |                                                |   | W0  zeta      2 |    | |
                                 |     /   /   2     2       \1/2 \   W0 zeta sinh| t | --------- - W0  |    | |
                 /   W0 t zeta \ |     |   | W0  zeta      2 |    |               \   \     4           /    / |
  Gain - Gain exp| - --------- | | cosh| t | --------- - W0  |    | + ---------------------------------------- |
                 \       2     / |     \   \     4           /    /             /   2     2       \1/2         |
                                 |                                              | W0  zeta      2 |            |
                                 |                                            2 | --------- - W0  |            |
                                 \                                              \     4           /            /
//*/
return Res;
}
